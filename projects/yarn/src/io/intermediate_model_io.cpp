std::ostream& operator<<(std::ostream& s, const intermediate_model& v) {
    boost::io::ios_flags_saver ifs(s);
    s.setf(std::ios_base::boolalpha);
    s.setf(std::ios::fixed, std::ios::floatfield);
    s.precision(6);
    s.setf(std::ios::showpoint);

    s << " { "
      << "\"__type__\": " << "\"dogen::yarn::intermediate_model\"" << ", "
      << "\"name\": " << v.name() << ", "
      << "\"origin_type\": " << v.origin_type() << ", "
      << "\"references\": " << v.references() << ", "
      << "\"leaves\": " << v.leaves() << ", "
      << "\"modules\": " << v.modules() << ", "
      << "\"concepts\": " << v.concepts() << ", "
      << "\"primitives\": " << v.primitives() << ", "
      << "\"enumerations\": " << v.enumerations() << ", "
      << "\"objects\": " << v.objects() << ", "
      << "\"exceptions\": " << v.exceptions() << ", "
      << "\"visitors\": " << v.visitors() << ", "
      << "\"injected_elements\": " << v.injected_elements() << ", "
      << "\"has_generatable_types\": " << v.has_generatable_types() << ", "
      << "\"indices\": " << v.indices() << ", "
      << "\"root_module\": " << v.root_module() << ", "
      << "\"input_language\": " << v.input_language() << ", "
      << "\"output_languages\": " << v.output_languages()
      << " }";
    return(s);
}

