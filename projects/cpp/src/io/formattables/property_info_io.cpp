#include <ostream>
#include <boost/io/ios_state.hpp>
#include <boost/algorithm/string.hpp>
#include "dogen/cpp/io/settings/opaque_settings_io.hpp"
#include "dogen/cpp/io/formattables/property_info_io.hpp"
